package com.walter.tapcounter;

public class MyQuotes {

	public String _quote;
	public String _preacher;
	public void setQuote(String quote)
	{
		_quote=quote;
	}
	public void setPreacher(String preacher)
	{
		_preacher=preacher;
	}
	public String getQuote()
	{
		return _quote;
	}	
	public String getPreacher()
	{
		return _preacher;
	}	
}
